    # class methods
    # used in cases where we dont need multiple instances
        # ex: hat1 = Hat()
        # hat2 = Hat()
class Hat:
    houses = ["Gryffindor", "Hufflepuff", "Ravenclaw", "Slytherin"]

    @classmethod
    def sort(cls, name):
        print(name, "is in", random.choice(cls.houses))

Hat.sort("Harry")